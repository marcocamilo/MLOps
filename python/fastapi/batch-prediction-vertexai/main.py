@app.post("/predict")
def predict(request: RequestBody):
    try:
        logger.info(
            f"ğŸ‘ Received prediction request with {len(request.instances)} instances"
        )

        if request.instances:
            logger.info(
                f"â„¹ï¸ First instance type: {type(request.instances[0])}, value: {request.instances[0]}"
            )

        predictions = process_batch_predictions(
            instances=request.instances,
            prediction_func=process_predictions,
            id_extractor=None,
            default_prediction=None,
        )

        return {"predictions": predictions}

    except Exception as e:
        logger.error(f"âŒ Error in /predict: {str(e)}", exc_info=True)
        raise HTTPException(status_code=500, detail=str(e))
